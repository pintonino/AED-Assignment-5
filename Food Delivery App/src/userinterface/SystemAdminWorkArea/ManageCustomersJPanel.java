    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRw = tblCustomerList.getSelectedRow();
        if (selectedRw < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        else{
            UserAccount custAccount = (UserAccount)tblCustomerList.getValueAt(selectedRw, 1);
            UpdateUserJPanel panel = new UpdateUserJPanel(userProcessContainer,custAccount,"Customers");
            userProcessContainer.add("UpdateUserJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRw = tblCustomerList.getSelectedRow();
        if (selectedRw < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        else{
            UserAccount custAccount = (UserAccount)tblCustomerList.getValueAt(selectedRw, 1);
            customerOrg.getUserAccountDirectory().deleteUserAccount(custAccount);
            customerOrg.getEmployeeDirectory().deleteEmployee(custAccount.getEmployee());
            JOptionPane.showMessageDialog(null, "Customer deleted successfully");
        } 
    }//GEN-LAST:event_btnDeleteActionPerformed

