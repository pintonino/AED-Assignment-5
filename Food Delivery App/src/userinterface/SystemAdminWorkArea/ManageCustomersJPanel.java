    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblCustomerList.getModel();
        dtm.setRowCount(0);
        Organization customerOrganziation = ecosystem.getCustomerDirectory().searchOrganization("Customer") ;
        for(UserAccount customerAccount: customerOrganziation.getUserAccountDirectory().getUserAccountList()) {
            Object row[] = new Object[3];
            row[0] = customerAccount.getEmployee().getName();
            row[1] = customerAccount;
            row[2] = customerAccount.getPassword();
            dtm.addRow(row);
        }
    }
    
